class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            BasicTemplateTheme {
                BasicTemplateApp()
            }
        }
    }
}

@PreviewScreenSizes
@Composable
fun BasicTemplateApp() {
    var currentDestination by rememberSaveable { mutableStateOf(AppDestinations.HOME) }

    NavigationSuiteScaffold(
        navigationSuiteItems = {
            AppDestinations.entries.forEach {
                item(
                    icon = {
                        Icon(
                            imageVector = it.icon(),
                            contentDescription = it.label,
                        )
                    },
                    label = { Text(it.label) },
                    selected = it == currentDestination,
                    onClick = { currentDestination = it },
                )
            }
        },
    ) {
        Scaffold(modifier = Modifier.fillMaxSize()) { innerPadding ->
            Greeting(
                name = "Android",
                modifier = Modifier.padding(innerPadding),
            )
        }
    }
}

enum class AppDestinations(
    val label: String,
    val icon: @Composable () -> ImageVector,
) {
    HOME(label = "Home", icon = { ImageVector.vectorResource(R.drawable.home_24px) }),
    FAVORITES("Favorites", icon = { ImageVector.vectorResource(R.drawable.favorite_24px) }),
    PROFILE("Profile", icon = { ImageVector.vectorResource(R.drawable.account_box_24px) }),
}

@Composable
fun Greeting(
    name: String,
    modifier: Modifier = Modifier,
) {
    Text(
        text = "Hello $name!",
        modifier = modifier,
    )
}

@Preview(showBackground = true)
@Composable
fun GreetingPreview() {
    BasicTemplateTheme {
        Greeting("Android")
    }
}
